package Shixun2018.entity;

public class VoteRecord {
int id;
int userid;
int player;
int fight;
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public int getUserid() {
	return userid;
}
public void setUserid(int userid) {
	this.userid = userid;
}
public int getPlayer() {
	return player;
}
public void setPlayer(int player) {
	this.player = player;
}
public int getFight() {
	return fight;
}
public void setFight(int fight) {
	this.fight = fight;
}

}
